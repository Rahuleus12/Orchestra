// ---------------------------------------------------------------------------
// Message Types
// ---------------------------------------------------------------------------

// Role represents the role of a message sender in a conversation.
type Role = message.Role

const (
	// RoleSystem represents system-level instructions that guide model behavior.
	RoleSystem = message.RoleSystem

	// RoleUser represents messages from the end user.
	RoleUser = message.RoleUser

	// RoleAssistant represents messages generated by the AI model.
	RoleAssistant = message.RoleAssistant

	// RoleTool represents messages containing tool execution results.
	RoleTool = message.RoleTool

	// RoleFunction represents messages containing function call results.
	RoleFunction = message.RoleFunction
)

// Message represents a single message within a conversation.
type Message = message.Message

// ContentBlock represents a single piece of content within a message.
type ContentBlock = message.ContentBlock

// ToolCall represents a single tool/function call requested by the model.
type ToolCall = message.ToolCall

// ToolCallFunction represents the function details within a tool call.
type ToolCallFunction = message.ToolCallFunction

// ToolResult holds the result of executing a tool call.
type ToolResult = message.ToolResult

// Conversation represents an ordered sequence of messages exchanged
// within a single interaction session with an LLM provider.
type Conversation = message.Conversation

// Message constructors

// TextMessage creates a new Message with a single text content block.
func TextMessage(role Role, text string) Message {
	return message.TextMessage(role, text)
}

// SystemMessage creates a new system message with the given text.
func SystemMessage(text string) Message {
	return message.SystemMessage(text)
}

// UserMessage creates a new user message with the given text.
