// ---------------------------------------------------------------------------
// Provider Types
// ---------------------------------------------------------------------------

// Provider is the core interface that all LLM backends must implement.
type Provider = provider.Provider

// GenerateRequest represents a request to generate a completion from a model.
type GenerateRequest = provider.GenerateRequest

// GenerateResult holds the result of a single completion generation.
type GenerateResult = provider.GenerateResult

// GenerateOptions configures how a model generates its response.
type GenerateOptions = provider.GenerateOptions

// GenerateOption is a functional option for configuring GenerateOptions.
type GenerateOption = provider.GenerateOption

// StreamEvent represents a single event in a streaming response.
type StreamEvent = provider.StreamEvent

// StreamEventType identifies the kind of event in a response stream.
type StreamEventType = provider.StreamEventType

const (
	// StreamEventStart is emitted once when streaming begins.
	StreamEventStart = provider.StreamEventStart

	// StreamEventChunk is emitted for each token or text fragment.
	StreamEventChunk = provider.StreamEventChunk

	// StreamEventToolCall is emitted when the model requests a tool call.
	StreamEventToolCall = provider.StreamEventToolCall

	// StreamEventDone is emitted once when streaming completes successfully.
	StreamEventDone = provider.StreamEventDone

	// StreamEventError is emitted when an error occurs during streaming.
	StreamEventError = provider.StreamEventError
)

// FinishReason indicates why the model stopped generating tokens.
type FinishReason = provider.FinishReason

const (
	// FinishReasonStop indicates the model generated a natural stop point.
	FinishReasonStop = provider.FinishReasonStop

	// FinishReasonLength indicates generation stopped due to max tokens.
	FinishReasonLength = provider.FinishReasonLength

	// FinishReasonToolCall indicates the model requested tool execution.
	FinishReasonToolCall = provider.FinishReasonToolCall

	// FinishReasonContentFilter indicates generation was stopped by content filter.
	FinishReasonContentFilter = provider.FinishReasonContentFilter

	// FinishReasonError indicates generation stopped due to an error.
	FinishReasonError = provider.FinishReasonError
)

// TokenUsage tracks token consumption for a single request.
type TokenUsage = provider.TokenUsage

// ModelCapabilities describes the features a specific model supports.
type ModelCapabilities = provider.ModelCapabilities

// ModelInfo describes a model available from a provider.
type ModelInfo = provider.ModelInfo

// ResponseFormat specifies how the model should format its output.
type ResponseFormat = provider.ResponseFormat

// ToolDefinition defines a tool that the model can invoke.
type ToolDefinition = provider.ToolDefinition

// FunctionDef describes a function that can be called by the model.
type FunctionDef = provider.FunctionDef

// ProviderError wraps an error from a provider with additional context.
type ProviderError = provider.ProviderError

// ProviderFactory is a function that creates a new Provider instance.
type ProviderFactory = provider.ProviderFactory

