// Package orchestra provides the public API for the Orchestra multi-agent
// AI orchestration engine.
//
// Orchestra is a Go-based framework for orchestrating multiple AI agents that
// use different LLM providers (OpenAI, Anthropic, Google Gemini, Ollama, etc.)
// and models. It provides a unified abstraction layer over heterogeneous LLM
// backends and enables complex multi-agent workflows.
//
// # Quick Start
//
// Register a provider and generate a completion:
//
//	p, err := orchestra.NewMockProvider("test")
//	if err != nil {
//	    log.Fatal(err)
//	}
//	result, err := p.Generate(ctx, orchestra.GenerateRequest{
//	    Model:    "mock-model",
//	    Messages: []orchestra.Message{orchestra.UserMessage("Hello!")},
//	})
//
// # Functional Options
//
// Use functional options to configure generation parameters:
//
//	opts := orchestra.NewGenerateOptions(
//	    orchestra.WithTemperature(0.7),
//	    orchestra.WithMaxTokens(1024),
//	    orchestra.WithJSONMode(),
//	)
//
// # Configuration
//
// Load configuration from YAML or environment variables:
//
//	cfg, err := orchestra.LoadConfig("configs/orchestra.yaml")
//	cfg := orchestra.LoadConfigFromEnv()
//
// # Provider Registry
//
// Use the global registry to manage providers:
//
//	orchestra.RegisterProvider("openai", factory, orchestra.ProviderConfig{...})
//	orchestra.AliasProvider("gpt4", "openai::gpt-4-turbo")
//	p, model, err := orchestra.ResolveModel("gpt4")
